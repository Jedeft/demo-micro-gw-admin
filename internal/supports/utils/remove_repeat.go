package utils

// RemoveUint32Duplication uint32 array 去重
func RemoveUint32Duplication(arr []uint32) []uint32 {
	set := make(map[uint32]struct{}, len(arr))
	j := 0
	for _, v := range arr {
		_, ok := set[v]
		if ok {
			continue
		}
		set[v] = struct{}{}
		arr[j] = v
		j++
	}
	return arr[:j]
}

